# this program will create a list that has number less than 10

original_list = [2, 8, 12, 24, 34, 4, 56, 43,7, 10, 9, 21, 1]
list_final = []
print("Here is the original list.")
print(original_list)

for numbers in original_list:
    if numbers < 10:
        list_final.append(numbers)
print("Here are the numbers less than 10: ")
print(list_final)

